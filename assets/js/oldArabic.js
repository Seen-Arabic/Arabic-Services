function replaceAll(oldch, newch, text) {
  for (i in text) {
    if (text[i] == oldch) {
      text.replace(oldch, newch);
    }
  }
  return text;
}

function strip_tashkeel(text) {
  if (text == null) {
    return text;
  }
  for (char in TASHKEEL) {
    try {
      text = text.replaceAll(TASHKEEL[char], "");
    } catch (err) {
      text = replaceAll(TASHKEEL[char], "", text);
    }
  }
  return text;
}

